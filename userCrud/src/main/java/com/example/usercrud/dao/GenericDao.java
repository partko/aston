package com.example.usercrud.dao;

import java.util.List;
import java.util.Optional;

/**
 * Обобщённый DAO-контракт для базовых CRUD-операций.
 *
 * @param <T>  тип сущности
 * @param <ID> тип идентификатора
 */
public interface GenericDao<T, ID> {

    /**
     * Сохраняет новую сущность в базе данных.
     *
     * @param entity сущность для сохранения
     * @return сохранённая сущность
     */
    T save(T entity);

    /**
     * Находит сущность по её идентификатору.
     *
     * @param id идентификатор сущности
     * @return Optional, содержащий найденную сущность, или пустой, если сущность не найдена
     */
    Optional<T> findById(ID id);

    /**
     * Возвращает список всех сущностей данного типа.
     *
     * @return список сущностей; будет пустой, если записей нет
     */
    List<T> findAll();

    /**
     * Обновляет существующую сущность в базе данных.
     *
     * @param entity сущность с обновлёнными данными
     * @return обновлённая сущность
     */
    T update(T entity);

    /**
     * Удаляет сущность по её идентификатору.
     *
     * @param id идентификатор сущности
     * @return true - если сущность была удалена, false - если сущность с таким идентификатором не найдена
     */
    boolean deleteById(ID id);
}